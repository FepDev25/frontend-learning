// Métodos de los String: Repeat, Split

const producto = "Monitor de 20 pulgadas";

// simplemente repite el texto n veces
console.log("Repeat")
const texto = "Felipe";
console.log(texto.repeat(5));

// Split convierte un String en un Array.
// El argumento que se le pasa es el separador, es decir, el carácter o conjunto de caracteres que se utilizará para dividir el String en partes. 
// El resultado es un Array de subcadenas.
console.log("Split")
console.log(producto.split(" "));
