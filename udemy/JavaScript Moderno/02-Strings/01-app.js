// Strings
// son un dato primitivo, almacenan texto, se pueden usar comillas simples, dobles o backticks

const producto = "Monitor de 20 pulgadas";
console.log(producto);

const producto2 = String('Monitor de 20 pulgadas');
console.log(producto2);

// El constructor String crea un objeto String, no es un dato primitivo, es un objeto
const producto3 = new String('Monitor de 32 pulgadas');
console.log(producto3)

const producto4 = 'Monitor de 50 pulgadas';
console.log(producto4)
