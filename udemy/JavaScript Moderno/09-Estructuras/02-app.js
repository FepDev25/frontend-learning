let puntaje = 1000;

if (puntaje === 1000){
    console.log("Es igual");
} else {
    console.log("No es igual");
}

puntaje = 2000;
if (puntaje === 1000){
    console.log("Es igual");
} else {
    console.log("No es igual");
}

puntaje = 3000;
if (puntaje !== 1000){
    console.log("Es diferente");
} else{
    console.log("No es diferente");
}