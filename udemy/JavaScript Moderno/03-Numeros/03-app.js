// Objeto Math en JavaScript

console.log("Valor de Pi: " + Math.PI);
console.log("Redondear 2.2 : " + Math.round(2.2));
console.log("Redondear 2.8 : " + Math.round(2.8));
console.log("Redondear hacia arriba 2.2 : " + Math.ceil(2.2));
console.log("Redondear hacia abajo 2.8 : " + Math.floor(2.8));
console.log("Raíz cuadrada de 144: " + Math.sqrt(144));
console.log("Valor absoluto de -78: " + Math.abs(-78));
console.log("Potencia 2**8: " + Math.pow(2,8));
console.log("Valor mínimo: " + Math.min(1, 5, 10, 90, 2));
console.log("Valor máximo: " + Math.max(1, 5, 10, 90, 2));
console.log("Valor random: " + Math.floor( Math.random() * 100 ))
