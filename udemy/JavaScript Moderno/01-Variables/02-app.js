// let permite declarar variables que pueden cambiar su valor a lo largo del tiempo

let producto = "Celular";
console.log(producto);

producto = "Tablet";
console.log(producto);

producto = null
console.log(producto);

producto = false
console.log(producto);

let numero = 30
console.log(numero);

numero = 3.1
console.log(numero);

numero = -20
console.log(numero);
