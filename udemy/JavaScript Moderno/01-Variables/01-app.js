console.log("Variables");

// Inicializar variables con valor
var producto = "Monitor de 24 pulgadas";
console.log(producto);

// Reasignar valor
producto = "Monitor de 12 pulgadas"
console.log(producto);

// Tipado dinámico
producto = 56
console.log(producto);

// Inicializar y luego dar valor
var disponible;
disponible = true
console.log(disponible);

// Inicializar múltiples
var num1 = 5,
    num2 = 10,
    num3 = 15
console.log(num1, num2, num3);

var nombre = "Felipe",
    apellido = "Peralta",
    edad = 21,
    pais = "Ecuador"
console.log(nombre, apellido, edad, pais);

// ver tipo de dato
console.log(typeof producto);
console.log(typeof disponible);
console.log(typeof num1);
console.log(typeof nombre);
